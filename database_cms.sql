-- 就业表
CREATE TABLE employment (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '就业记录ID',
    resident_id BIGINT NOT NULL COMMENT '居民ID',
    resident_name VARCHAR(50) NOT NULL COMMENT '居民姓名',
    id_card VARCHAR(18) NOT NULL COMMENT '居民身份证号',
    company VARCHAR(100) COMMENT '工作单位',
    position VARCHAR(50) COMMENT '职位',
    salary DECIMAL(10,2) COMMENT '薪资',
    employment_status VARCHAR(20) COMMENT '就业状态（在职、失业、退休等）',
    industry VARCHAR(50) COMMENT '行业',
    start_date DATE COMMENT '入职日期',
    contract_type VARCHAR(20) COMMENT '合同类型（固定期限、无固定期限等）',
    notes TEXT COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    created_by BIGINT NOT NULL COMMENT '创建人ID',
    FOREIGN KEY (resident_id) REFERENCES residents(id) ON DELETE CASCADE,
    FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_resident_id (resident_id),
    INDEX idx_id_card (id_card),
    INDEX idx_company (company),
    INDEX idx_employment_created_by (created_by)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '就业表';

-- 社保表
CREATE TABLE social_security (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '社保记录ID',
    resident_id BIGINT NOT NULL COMMENT '居民ID',
    resident_name VARCHAR(50) NOT NULL COMMENT '居民姓名',
    id_card VARCHAR(18) NOT NULL UNIQUE COMMENT '居民身份证号',
    pension_insurance VARCHAR(20) COMMENT '养老保险',
    medical_insurance VARCHAR(20) COMMENT '医疗保险',
    unemployment_insurance VARCHAR(20) COMMENT '失业保险',
    work_injury_insurance VARCHAR(20) COMMENT '工伤保险',
    maternity_insurance VARCHAR(20) COMMENT '生育保险',
    insurance_status VARCHAR(20) COMMENT '参保状态',
    payment_amount DECIMAL(10,2) COMMENT '缴费金额',
    start_date DATE COMMENT '参保日期',
    end_date DATE COMMENT '停保日期',
    notes TEXT COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    created_by BIGINT NOT NULL COMMENT '创建人ID',
    FOREIGN KEY (resident_id) REFERENCES residents(id) ON DELETE CASCADE,
    FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_resident_id (resident_id),
    UNIQUE INDEX idx_id_card (id_card),
    INDEX idx_social_security_created_by (created_by)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '社保表';

-- 房产表
CREATE TABLE property (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '房产记录ID',
    resident_id BIGINT NOT NULL COMMENT '居民ID',
    resident_name VARCHAR(50) NOT NULL COMMENT '居民姓名',
    id_card VARCHAR(18) NOT NULL COMMENT '居民身份证号',
    property_type VARCHAR(20) COMMENT '房产类型（商品房、经济适用房、农村自建房等）',
    address VARCHAR(200) COMMENT '房产地址',
    area DECIMAL(8,2) COMMENT '面积（平方米）',
    value DECIMAL(12,2) COMMENT '估值',
    property_certificate VARCHAR(50) COMMENT '产权证号',
    acquisition_date DATE COMMENT '取得日期',
    usage_type VARCHAR(20) COMMENT '使用类型（自住、出租、闲置等）',
    rooms INT COMMENT '房间数',
    notes TEXT COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    created_by BIGINT NOT NULL COMMENT '创建人ID',
    FOREIGN KEY (resident_id) REFERENCES residents(id) ON DELETE CASCADE,
    FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_resident_id (resident_id),
    INDEX idx_id_card (id_card),
    INDEX idx_property_created_by (created_by)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '房产表';

-- 车辆表
CREATE TABLE vehicle (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '车辆记录ID',
    resident_id BIGINT NOT NULL COMMENT '居民ID',
    resident_name VARCHAR(50) NOT NULL COMMENT '居民姓名',
    id_card VARCHAR(18) NOT NULL COMMENT '居民身份证号',
    license_plate VARCHAR(20) COMMENT '车牌号',
    vehicle_type VARCHAR(20) COMMENT '车辆类型（轿车、SUV、货车等）',
    brand VARCHAR(50) COMMENT '品牌',
    model VARCHAR(50) COMMENT '型号',
    color VARCHAR(20) COMMENT '颜色',
    engine_number VARCHAR(50) COMMENT '发动机号',
    chassis_number VARCHAR(50) COMMENT '车架号',
    purchase_date DATE COMMENT '购买日期',
    purchase_price DECIMAL(10,2) COMMENT '购买价格',
    insurance_expiry_date DATE COMMENT '保险到期日期',
    annual_inspection_date DATE COMMENT '年检日期',
    notes TEXT COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    created_by BIGINT NOT NULL COMMENT '创建人ID',
    FOREIGN KEY (resident_id) REFERENCES residents(id) ON DELETE CASCADE,
    FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_resident_id (resident_id),
    INDEX idx_license_plate (license_plate),
    INDEX idx_vehicle_created_by (created_by)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '车辆表';

