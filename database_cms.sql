-- 重新初始化人口普查管理系统数据库
-- 删除并重新创建数据库

DROP DATABASE IF EXISTS population;
CREATE DATABASE population DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE population;

-- 设置连接字符集，确保中文数据正确插入
SET NAMES utf8mb4;
SET CHARACTER SET utf8mb4;
SET collation_connection = utf8mb4_unicode_ci;

-- 用户表
CREATE TABLE users (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '用户ID',
    username VARCHAR(50) NOT NULL UNIQUE COMMENT '用户名',
    password VARCHAR(100) NOT NULL COMMENT '密码',
    email VARCHAR(100) COMMENT '邮箱',
    role VARCHAR(20) NOT NULL DEFAULT 'USER' COMMENT '角色（ADMIN管理员，USER普通用户）',
    phone VARCHAR(20) COMMENT '电话',
    id_card VARCHAR(18) COMMENT '身份证号',
    householdId BIGINT COMMENT '户籍ID',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    last_login_time DATETIME COMMENT '最后登录时间',
    is_active TINYINT(1) DEFAULT 1 COMMENT '是否激活（1是，0否）',
    INDEX idx_username (username),
    INDEX idx_id_card (id_card)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '用户表';

-- 户籍表（使用简单的数字户籍编号）
CREATE TABLE households (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '户籍ID',
    household_number VARCHAR(20) NOT NULL UNIQUE COMMENT '户籍编号',
    address VARCHAR(200) NOT NULL COMMENT '地址',
    householder_name VARCHAR(50) NOT NULL COMMENT '户主姓名',
    householder_id_card VARCHAR(18) NOT NULL COMMENT '户主身份证号',
    phone_number VARCHAR(20) COMMENT '联系电话',
    registration_date DATE COMMENT '登记日期',
    population_count INT DEFAULT 1 COMMENT '人口数量',
    notes TEXT COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    created_by BIGINT NOT NULL COMMENT '创建人ID',
    INDEX idx_household_number (household_number),
    INDEX idx_householder_name (householder_name),
    INDEX idx_householder_id_card (householder_id_card),
    INDEX idx_households_created_by (created_by),
    FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '户籍表';

-- 居民表
CREATE TABLE residents (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '居民ID',
    name VARCHAR(50) NOT NULL COMMENT '姓名',
    id_card VARCHAR(18) NOT NULL UNIQUE COMMENT '身份证号',
    gender VARCHAR(10) COMMENT '性别',
    birth_date DATE COMMENT '出生日期',
    household_id BIGINT NOT NULL COMMENT '户籍ID',
    relationship VARCHAR(20) COMMENT '与户主关系（户主、配偶、子女、父母等）',
    ethnic_group VARCHAR(20) COMMENT '民族',
    education_level VARCHAR(20) COMMENT '教育程度',
    occupation VARCHAR(50) COMMENT '职业',
    marital_status VARCHAR(20) COMMENT '婚姻状况（未婚、已婚、离异、丧偶）',
    phone_number VARCHAR(20) COMMENT '联系电话',
    is_householder TINYINT(1) DEFAULT 0 COMMENT '是否户主（1是，0否）',
    notes TEXT COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    created_by BIGINT NOT NULL COMMENT '创建人ID',
    FOREIGN KEY (household_id) REFERENCES households(id) ON DELETE CASCADE,
    FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_id_card (id_card),
    INDEX idx_name (name),
    INDEX idx_household_id (household_id),
    INDEX idx_residents_created_by (created_by)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '居民表';

-- 教育表
CREATE TABLE education (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '教育记录ID',
    resident_id BIGINT NOT NULL COMMENT '居民ID',
    resident_name VARCHAR(50) NOT NULL COMMENT '居民姓名',
    id_card VARCHAR(18) NOT NULL COMMENT '居民身份证号',
    school_name VARCHAR(100) COMMENT '学校名称',
    education_level VARCHAR(20) COMMENT '教育程度',
    major VARCHAR(50) COMMENT '专业',
    enrollment_date DATE COMMENT '入学日期',
    graduation_date DATE COMMENT '毕业日期',
    status VARCHAR(20) COMMENT '状态（在读、毕业、辍学、休学）',
    notes TEXT COMMENT '备注',
    create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    created_by BIGINT NOT NULL COMMENT '创建人ID',
    FOREIGN KEY (resident_id) REFERENCES residents(id) ON DELETE CASCADE,
    FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE CASCADE,
    INDEX idx_resident_id (resident_id),
    INDEX idx_id_card (id_card),
    INDEX idx_school_name (school_name),
    INDEX idx_education_created_by (created_by)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT '教育表';

-- 医疗表
CREATE TABLE medical (
    id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT '医疗记录ID',
    resident_id BIGINT NOT NULL COMMENT '居民ID',
    resident_name VARCHAR(50) NOT NULL COMMENT '居民姓名',
